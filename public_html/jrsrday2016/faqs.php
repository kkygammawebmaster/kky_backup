<p class="head">Husky Band FAQs</p>

<b>Where does the band rehearse?</b><br>
We rehearse in the Dempsey Indoor field, East Field or in Husky Stadium. It really depends on when and where the other athletic teams practice.
<br></br>

<b>When does the band rehearse?</b><br>
We have rehearsal from 6-8PM Monday through Thursday, No rehearsal on Wed and rehearsal 5-7PM on Friday. On game days the band usually reports two hours before kick off.
<br></br>

<b>How many shows do you perform per year?</b><br>
We are one of the few university band programs that still perform a new halftime show for every home game.
<br><br>

<b>My high school did not do field shows, will that be a problem?</b><br>
No it will not be. Before the season starts and right after your audition the band has pre-school drills week where the vets teach the incoming rookies everything they need to know about Husky Band.
<br></br>

<b>Do I have to be a Music Major?</b><br>
No, we members who have majors ranging from A-Z.
<br></br>

<b>How many band directors has the Husky Band have?</b><br>
We are lucky enough to only have 4 directors for the past 82 years. Starting in order Walter Welke, Bill Cole, Bill Bissell and currently Dr. Brad McDavid.
<br></br>

<b>How old is the Husky Band?</b><br>
We are about to enter the Bands 82nd year.
<br></br>

<b>How do I audition?</b><br>
The Drumline will be having auditions on Saturday May 14th, 2011 (more information can be found at the <a href="http://depts.washington.edu/drumpage/" target="_blank">here at their website</a>), while the rest of the band auditions on Sunday September 4th, 2011. It will consist of 2 scales (1 random and one chromatic), one page of exercises, and 3 stands tunes that the Husky Band plays. There will be a marching portion in the afternoon and results will be posted at the end of the day.
<br></br>

<b>Can I be a part of the Husky Band if I do not go to the University of Washington?</b><br>
Yes, you just have to do the band through an extension program.
<br></br>

<b>Where does the band travel?</b><br>
The Husky Band is lucky enough to be  a part of the athletic department and as such we send a pep band to each away game during the football season. There are full band trips to WSU for the Apple Cup, and the two Oregon schools (University of Oregon and Oregon State University).
<br></br>

